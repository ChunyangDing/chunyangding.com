<p> Acts 24 reads like a legal case summary, with the opening argument by the Tertullus and the elders, followed by Paul's defense, and concluding with Felix's judgement. Tertullus seemed to mainly be flattering Felix, appealing to his "excellence" - it reminds me of John 19, where the chief priests of Jerusalem were willing to accept Roman rule over them than to accept the rightful rule of Jesus as Lord. ( "But they shouted, “Take him away! Take him away! Crucify him!”“Shall I crucify your king?” Pilate asked. “We have no king but Caesar,” the chief priests answered. Finally Pilate handed him over to them to be crucified." - John 19:15-16). </p>

<p> I really like the way that Paul just clearly states what he has done, without trying to really argue about it, just like how Eric mentions about Paul in Acts 23. Paul doesn't really appeal to trying to see his own way, but trusts that God will provide with whatever is the best way. I so frequently default to a mentality where I need to justify what I'm doing, rather than trusting the judgment of God instead. Even as I feel myself grow spiritually in trusting God's word, I sometimes still cling on to old reassurances (other people's approval, financial stability, work completion etc) for my own justification that I am doing well. </p>

<p> I'm not exactly sure of what to make of Felix - the chapter says that he is "familiar with the Way", but he still acts in some questionable ways. I guess it's another example of where God is able to use all kinds of flawed humans to fulfill His plan. </p>

