<center> <h1> December 3 -- Acts 25 </h1> </center>

<p> Hi all! Great catching up with you guys this past weekend. </p>

<p> Acts 25 is a fairly matter of fact chapter with a lot of insights into the legal workings of Paul's time. (almost like it's Law & Order: Roman Empire) </p>

<p> Anyway, at the start of 25 Felix is no longer in charge and has given way to Festus. However, both men exhibit the same weaknesses. At the end of 24, Felix leaves Paul behind in jail as a favor to the Jews as he leaves his post. In 25, Festus confesses later on that "I found he [Paul] had done nothing deserving death" but that does not stop him from doing the Jews a favor by trying in v 9-11 to bait Paul into requesting a Jerusalem trial so Paul's Jewish opponents ambush Paul otw to Jerusalem.  </p>

<p> It struck me as I was reading that I was completely unsurprised by Festus' actions. In some ways we expect that many figures our authority will act like ruthless, self interested individuals unconcerned with doing right, and it only makes the contrast with our eternally just heavenly king all the more prominent. </p>

<p> So Paul gets around this trap of a Jerusalem trial by appealing to his Roman citizenship. He demands to be tried by a Roman authority instead, and in 26 he will make his case before King Agrippa II. We see here God's gift of identity (making Paul a Roman citizen) and wisdom (giving Paul the knowledge of the rights that Roman citizenship conferred upon him) to deliver Paul from his situation.  </p>

<p> That's all from me for 25. Hope you guys have a good week and looking forward to hearing from yall.  </p>

