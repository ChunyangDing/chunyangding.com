<center> <h1> July 8 -- Acts 10 </h1> </center>

<p> Hey guys, I just got back from my road trip through the pacific northwest! It was such a beautiful trip, and I saw so much amazing nature! Also had a great time meeting with my brother’s friends, getting to know his college friends and church friends. I’m attaching a couple photos here (crater lake, Olympic national park, gum wall in portland)! </p>

<p> Now onto acts 10: this was a really interesting passage, in part because of God’s divine orchestration of events, the interesting dreams, and the good news that results from it! </p>

<p> 1.      Cornelius. He prayed continually to God, and the angel tells him “your prayers and your alms have ascended as a memorial before God.” That’s neat. I generally don’t think of prayers and donations to the needy as a sacrifice to God, but it’s clear that his man’s heart is remembered by God (v different from simon the the magician from ch. 8). He is told to get Peter, and he obeys. </p>

<p> 2.      Peter dreams about the clean/unclean animals. At a minimum, this represents the overturning of dietary conventions, but it’s also a very confusing passage. I also find it interesting that this vision prepares him to go with the gentile visitors, who he normally might not have been willing to associate with. He’s brought to Cornelius, who worships Peter. But peter quickly states that he is just a man – as the ESV study bible notes, this is very different from Herod’s response later in acts 12. Herod dies on the spot. </p>

<p> 3.      v. 34-43 is a nice summary of the Gospel. And it’s a presentation of the good news to the gentiles. I found it interesting that Peter would come to the conclusion that the gospel is for all, just from a dream about which animals he could eat. But the gentiles hear the good news, and then the holy spirit falls on the gentiles, and they are baptized. That’s a pretty powerful confirmation from God that this is the intent of the gospel, and that “everyone who believes in him received forgiveness of sins through his name.” </p>
<p> These are such powerful images of fellowship 😊 </p>

