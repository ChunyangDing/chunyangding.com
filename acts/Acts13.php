<center> <h1> July 14 -- Acts 13 </h1> </center>

<p> Hello friends! </p>

<p> Ah, it looks like we are all slipping a bit behind, but it's still great that we have this email chain going :) almost halfway through Acts! </p>

<p> I was thinking about what the informal name for this chain became (reacts) and was struck by how different of a mentality these early Christians had. They were not really reacting to anything in their world, but instead, taking the initiative to "go and make disciples of all nations, baptizing them in the name of the Father and of the Son and of the Holy Spirit, and teaching them to obey everything I have commanded you" (Matthew 28:19-20). We see Paul and Barnabas setting out on this journey, reaching the people of Cyprus and Antioch, calling out false prophets, and preaching the Good News. </p>

<p> The church that I am attending right now is very focused on evangelism, outreach, and church planting, so the pastor will often weave those themes into the sermons. Even after becoming more comfortable with speaking about my faith to relative strangers, I frequently still worry if I'm doing a good job or not. But honestly, this section of Acts 13:16-41 does about as good of a job as possible in getting to the heart of the message. From the history of the Jewish nation, to redemption in Christ, to His resurrection, Paul really delivers. And it works! In verses 43-45, we see that "when the congregation was dismissed, many of the Jews and devout converts to Judaism followed Paul and Barnabas, who talked with them and urged them to continue in the grace of God. On the next Sabbath, almost the whole city gathered to hear the word of the Lord." Isn't that incredible, that Paul's message was able to move the entire city of Antioch out to attend a second service? I think I often doubt that I have it in me to do anything of the same magnitude. And even after they were expelled from Antioch, Paul and Barnabas take it with grace, as Jesus commanded them - to shake off the dust of their boots and to move on. </p>

<p> Looking forwards to our call tomorrow! </p>
<p> ~Chunny </p>

