<p> Hi John/all!  </p>

<p> Some thoughts in response:  </p>

<p> 1) I thought the contrast/comparison between the city clerk in Acts 19 and Gallio of Corinth in Acts 18 is interesting. Both cases are of public officials intervening in a way that affects this early band of missionaries. While Gallio turns away from the "internal" dispute, the city clerk quiets the rioting crowd in Epheus. I think my takeaway is the same as John's regarding the businessmen - although sometimes it may seem that the actions of the world are aligned with Christian teaching, it is hard to discern truthfully their beliefs, or if their actions are only in self interest. Even though we see that the Lord uses everyone in His great plan, I think it is good to be aware of misaligned interests and not confuse it together!  </p>

<p> 2) The exorcists actually brought to mind Simon the magician from chapter 8, in the sense it appears that both parties are more interested in the power of Christ and unwilling to submit to christ at the same time?  </p>

