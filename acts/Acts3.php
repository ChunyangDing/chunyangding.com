	<p> Hi guys! Hope y'all are doing well! And thanks to Chunny and Eric for sharing the first 2 days :) </p>

	<p> Here's my reflection on Acts 3:
	<p> 1) To Eric's point yesterday about Peter quoting the Old Testament, in chapter 3 Peter does the same thing again when talking to the crowd after healing the lame beggar. Peter tells them (ESV translation used)</p>

	<p> Moses said, ‘The Lord God will raise up for you a prophet like me from your brothers. You shall listen to him in whatever he tells you. 23 And it shall be that every soul who does not listen to that prophet shall be destroyed from the people.’</p>

	<p> And then Peter concludes with the observation that this "prophet" is Jesus, and that people should repent from their previous hostility to Jesus and follow him. Similar to Eric's reflection, sometimes I also fall into the tendency of thinking of the NT and OT as fairly distinct. But I think for me at least that's a result of me not knowing the OT very well, and the fact that Peter draws on the OT so heavily as "proof" in favor of Jesus reminds me of how God is a consistent and unchanging presence in the world. </p>

	<p> 2) Another thing that's striking from reading Acts, chapter 3 included, is the boldness with which the disciples preach about Jesus.</p>

	<p> But Peter said, “I have no silver and gold, but what I do have I give to you. In the name of Jesus Christ of Nazareth, rise up and walk!” </p>

	<p> I think it's safe to say I probably wouldn't have the courage to say anything like that in public :p But I think Peter and John's boldness in this chapter actually very closely reflects the boldness of Jesus' ministry (e.g raising Lazarus from the dead, telling the Samaritan woman about her past, rebuking the crowd after miraculously feeding them, addressing Zacchaeus).</p>

	<p> This is more of an unresolved question than a concrete point, but: what is the source of their boldness, and why do we not see this same kind of boldness today in Christian communities? and what does that say about us?</p>

	<p> Kind of unrelated, but I've been reading <a href='https://www.amazon.com/Every-Good-Endeavor-Connecting-Your/dp/1594632820'>Every Good Endeavor by Tim Keller</a>, and I'd recommend if anyone is interested in reading more about work & faith. The book does a good job of discussing the different narratives of how the church has traditionally seen work. Also if you wanna read it w/o buying I can share my Kindle account lol </p>

	<p> Cheers,</p>
