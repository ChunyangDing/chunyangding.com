<p> Hey guys, sorry this is a bit late! Traveling/vacation always takes a surprising amount of energy... Maybe I'm just getting old. Anyways, thanks for the thoughtful comments on acts, and I find it absolutely amazing that you visited Ephesus, Tuna! </p>

<p> Anyways, here are some more re:acts to the passage :) *Apologies for typos and brevity; typed on my phone </p>

<p> 1. We see an issue raised here, where the widows arent being taken care of by the church (possible neglected?). There's the conflict between the Greek and Hebraic Jews, just as the disciples were increasing in number. I think some commentaries say this points to a deeper social unrest/divide in the community, but if we read it as-is, then at the very least people aren't being taken care of. Their response is to delegate the work to those who were full of the holy Spirit to take care of it, while continuing in ministry themselves. We see in v.7 that as a result, the work of God continues to spread and many more believe! I take this passage to be a lesson to our church, which is growing quite fast and is encountering many social and cultural issues -- the church should be responsive and also continue in the primary purpose of the church, which is the teaching of God's word... I'll think more abt that later (race, class, gender, sexuality, nationality)... </p>

<p> 2. Stephen is arrested... Dang he's just trying his best and is filled with wisdom and the Spirit, and they can't argue their way past him. So they send up people to falsely accuse him, twisting the message in v.14: Jesus didn't say he would destroy the temple and he didn't abolish the law. He would fulfill it! This reminds me of Matthew 26:60 onwards, where Jesus is on trial and they bring up false witnesses to attack him.  </p>
<p> 3. It'll be interesting to study the response of both Stephen and Jesus, when enemies bring false accusations against the church. They are both unique figures in the Bible, but I think we may learn something from how they respond when they are unjustly accused for their faith. I think that we're bound to run into opposition and accusation if we are bold in our faith </p>
<p> Eric </p>

