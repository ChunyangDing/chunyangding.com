<p> Onto Acts 23. I think that as Chunny said, Paul's continuing in boldness, and he shows his confidence in the Lord's guidance. Increasingly, we see God's hand at work in Paul's life and his testimony. </p>
<p> In the beginning of ch 23, he is struck before council, on orders from high priest Ananias. It's interesting that even though Paul's initial reaction is one of frustration, he does recognize the authority of the high priest as a ruler over the people.  </p>
<p> I think that even more than Paul's actions and boldness, Acts 23 shows us how God orchestrates things behind the scenes to protect him and to help him in his ministry. From this chapter: the disagreement between Pharisees/Sadducees gives him opportunity to be taken under the tribune's protection, his nephew hears of the plot to ambush/kill him, and he is sent to Felix where his ministry will continue. </p>

<p> I find it particularly interesting that through this turmoil, Paul doesn't flinch or draw back from his commitment to God in any of these situations. Maybe it's because God has prepared Paul with the right background and the right set of experiences for his ministry, maybe it's because Paul has the esther-like attitude toward his life of ministry that "If I perish, I perish."  </p>

