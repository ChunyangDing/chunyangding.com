<p> Some thoughts on Acts 19: </p>
<p> 1-7: Paul meets some followers of John the Baptist and baptizes them in Jesus' name. The story reminded me of how God uses steps of faith in the present for his will, even if a believer's obedience only comes to fruition at a later date. By this point, it's been a while since John the Baptist's ministry. He's already been executed by this point, but because of the faithfulness of his ministry his believers in this chapter are receptive to hearing Paul and ultimately receiving Jesus. Also of note is that these men hung on to the truth about "the one who was to come after him, that is, Jesus" even after their teacher's tragic death, in the same way Jesus' disciples persevered after Jesus' death (although they had the benefit of Jesus' resurrection :p) </p>

<p> 11-20: Some Jewish exorcists try to call out an evil spirit in Jesus' name after God performs amazing miracles via Paul. The twist is the evil spirit is unafraid of them. In a real horror movie moment, it replies back "Jesus I know, and Paul I recognize, but who are you?" and attacks the men. I'm still in processing re: how to interpret this passage, because it also calls to mind Mark 9, pasted below in part </p>

<p> "38 John said to him, ‚ÄúTeacher, we saw someone casting out demons in your name,6 and we tried to stop him, because he was not following us.‚Äù 39 But Jesus said, ‚ÄúDo not stop him, for no one who does a mighty work in my name will be able soon afterward to speak evil of me. 40 For the one who is not against us is for us. 41 For truly, I say to you, whoever gives you a cup of water to drink because you belong to Christ will by no means lose his reward." </p>

<p> For now, I think my read is that the Jewish exorcists in Acts 19 were invoking Jesus' name not because they believed in him, but only because they saw it had been effective w/ Paul and were merely trying to copy him. They had none of Jesus' authority in them. Whereas perhaps the people discussed in Mark 9 may not have been part of Jesus' inner circle, but nevertheless they understood & believed a core part of Jesus' teachings. </p>

<p> 21-39: Some craftsmen start a riot to kick out Paul & friends since their business creating statues of gods/goddesses is under threat. Reminder that often commercial interests != God's interests, and we need to be discerning when it comes to that (esp. for someone like me working in industry lol). Also interesting is that intentionally or not, the town clerk ends up being a blessing to Paul & co by calming down the crowd </p>

