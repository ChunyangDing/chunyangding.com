				<h2 id="turkey"> Turkey </h2>
				
				<p> I subscribe to Alton Brown's Good Eats version of the turkey, which is primarily a brined turkey that is cooked at high temperature. I highly recommend watching the Youtube videos - they are very instructional! </p>
				
				<p> Ingredients </p>
				<ul>
					<li> Turkey, defrosted for 1 day per 5 pounds. </li>
					<li> Bucket </li>
					<li> 1 gallon (ish) vegetable broth </li>
					<li> 1 gallon water with ice </li>
					<li> 1 cup salt </li>
					<li> 1/2 cup brown sugar </li>
					<li> Peppercorn, allspice, ginger </li>
					<li> Apple, onion, rosemary, sage, thyme, cinnamon stick </li>
					<li> Large, 16x12x3 pan, with a small flat rack that fits inside. </li>
					<li> Meat thermometer </li>
				</ul>
				
				<p> Instructions </p>
				<ol>
					<li> The night before, prepare your brine. Bring the vegetable broth, salt, sugar, and spices to a boil. Allow the brine to fully boil before turning off heat and allowing it to cool. Note - this liquid is incredibly salty, as it should be! Don't taste it - you'll just gag at the taste. </li>
					<li> Prepare your turkey, making sure to remove the neck and giblet pouch from the inside cavity of the turkey. </li>
					<li> In the large bucket, pour the cooled brine in, and gently lower in the turkey. It might be slightly better to go breast first, but it should not matter much. Fill with iced water until covered, and leave overnight. </li>
					<li> Roughly 5 hours before your meal, go ahead and bring the turkey out and pat it dry. Insert a meat thermometer into the center of the thickest part of the breast, not hitting bone. Begin preheating your oven to 500 degrees. </li>
					<li> Halve the onion and apple, and place in bowl of water to microwave for 2 mins. Stuff them into the cavity of the turkey, along with the remaining aromatics. </p> 
					<li> Using aluminum foil, mold a triangle to cover the main breast/white meat of the turkey, but set it aside for now. Brush the entire turkey with olive oil and place in your deep pan. </p>
					<li> Pop the turkey into the oven until the skin turns golden brown, about 30-50 minutes (depending on your oven). Afterwards, put the premade aluminum foil onto the turkey, to prevent the white meat from cooking too rapidly. Feel free to drain off some of the turkey drippings from your pan when you do this as well. </li>
					<li> Set your meat thermometer to 161 degrees and remove turkey when that temperature is reached. Allow the turkey to rest for 20-30 mins - you should notice the temperature of the turkey to continue to rise until about 165. 
					<li> For carving... I'm not great. But, it seems like you can get decent chunks of white meat by starting at the centerbone and carving down in slices, and to also roughly remove the turkey legs. </li>
				</ol> 
								
