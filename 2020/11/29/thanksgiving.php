				<h2 id="broth"> Vegetable Broth </h2>
				
				<p> This is a very useful thing to prep for thanksgiving, and honestly, for any time that you have spare vegetables in your fridge that are about to go bad. Specifically for thanksgiving, I like to brine my turkey with a base that has vegetable broth in it, so I make a batch ahead of time. </p>
				
				<p> Ingredients </p>
				
				<ul> 
					<li> Olive oil </li>
					<li> Onions, celery, carrots, garlic </li>
					<li> Salt and pepper to taste </li>
					<li> Bay leaves </li>
					<li> Mushroom ends, spring onion, whole peppercorn, potato skins... anything that is scrap or leftover </li>
				</ul>
				
				<p> Instructions </p> 
				
				<ol>
					<li> Roughly chop all of your vegetables, and prepare a very large pot with water. </li>
					<li> Throw all of your vegetables in the pot and bring to a boil. Keeping the pot covered will slightly accelerate your process. </li>
					<li> After the pot is boiling, turn down the heat to a simmer. That's about it - there really isn't much else you need to do. Keep the heat on and add additional water as necessary, until you are satisfied with the taste. I usually cook it until all of the vegetables have turned to mush, usually at least an hour. </li>
					<li> In the sink, strain out all of the broth into a larger pot/bowl. </li>
				</ol>
				
				<p> Specifically for thanksgiving, you want to use this vegetable broth as a starter for the brine for your turkey - which is covered in the next recipe! </p>
				
