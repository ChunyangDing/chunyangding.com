				<p> Vegetable Broth </p>
				
				<p> This is a very useful thing to prep for thanksgiving, and honestly, for any time that you have spare vegetables in your fridge that are about to go bad. Specifically for thanksgiving, I like to brine my turkey with a base that has vegetable broth in it, so I make a batch ahead of time. </p>
				
				<p> Ingredients </p>
				
				<ul> 
					<li> Olive oil </li>
					<li> Onions, celery, carrots, garlic </li>
					<li> Salt and pepper to taste </li>
					<li> Bay leaves </li>
					<li> Mushroom ends, spring onion, whole peppercorn, potato skins... anything that is scrap or leftover </li>
				</ul>
				
				<p> Instructions </p> 
				
				<ol>
					<li> Roughly chop all of your vegetables, and prepare a very large pot with water. </li>
					<li> Throw all of your vegetables in the pot and bring to a boil. Keeping the pot covered will slightly accelerate your process. </li>
					<li> After the pot is boiling, turn down the heat to a simmer. That's about it - there really isn't much else you need to do. Keep the heat on and add additional water as necessary, until you are satisfied with the taste. I usually cook it until all of the vegetables have turned to mush, usually at least an hour. </li>
					<li> In the sink, strain out all of the broth into a larger pot/bowl. </li>
				</ol>
				
				<p> Specifically for thanksgiving, you want to use this vegetable broth as a starter for the brine for your turkey - which is covered in the next recipe! </p>
				
				<p> Turkey </p>
				
				<p> I subscribe to Alton Brown's Good Eats version of the turkey, which is primarily a brined turkey that is cooked at high temperature. I highly recommend watching the Youtube videos - they are very instructional! </p>
				
				<p> Ingredients </p>
				<ul>
					<li> Turkey, defrosted for 1 day per 5 pounds. </li>
					<li> Bucket </li>
					<li> 1 gallon (ish) vegetable broth </li>
					<li> 1 gallon water with ice </li>
					<li> 1 cup salt </li>
					<li> 1/2 cup brown sugar </li>
					<li> Peppercorn, allspice, ginger </li>
					<li> Apple, onion, rosemary, sage, thyme, cinnamon stick </li>
					<li> Large, 16x12x3 pan, with a small flat rack that fits inside. </li>
					<li> Meat thermometer </li>
				</ul>
				
				<p> Instructions </p>
				<ol>
					<li> The night before, prepare your brine. Bring the vegetable broth, salt, sugar, and spices to a boil. Allow the brine to fully boil before turning off heat and allowing it to cool. Note - this liquid is incredibly salty, as it should be! Don't taste it - you'll just gag at the taste. </li>
					<li> Prepare your turkey, making sure to remove the neck and giblet pouch from the inside cavity of the turkey. </li>
					<li> In the large bucket, pour the cooled brine in, and gently lower in the turkey. It might be slightly better to go breast first, but it should not matter much. Fill with iced water until covered, and leave overnight. </li>
					<li> Roughly 5 hours before your meal, go ahead and bring the turkey out and pat it dry. Insert a meat thermometer into the center of the thickest part of the breast, not hitting bone. Begin preheating your oven to 500 degrees. </li>
					<li> Halve the onion and apple, and place in bowl of water to microwave for 2 mins. Stuff them into the cavity of the turkey, along with the remaining aromatics. </p> 
					<li> Using aluminum foil, mold a triangle to cover the main breast/white meat of the turkey, but set it aside for now. Brush the entire turkey with olive oil and place in your deep pan. </p>
					<li> Pop the turkey into the oven until the skin turns golden brown, about 30-50 minutes (depending on your oven). Afterwards, put the premade aluminum foil onto the turkey, to prevent the white meat from cooking too rapidly. Feel free to drain off some of the turkey drippings from your pan when you do this as well. </li>
					<li> Set your meat thermometer to 161 degrees and remove turkey when that temperature is reached. Allow the turkey to rest for 20-30 mins - you should notice the temperature of the turkey to continue to rise until about 165. 
					<li> For carving... I'm not great. But, it seems like you can get decent chunks of white meat by starting at the centerbone and carving down in slices, and to also roughly remove the turkey legs. </li>
				</ol> 
				
				<p> Stuffing </p>
				
				<p> Stuffing is definitely my favorite part of the thanksgiving meal - it has a lovely amount of variety, and you can spruce it up however you like! </p>
				
				<p> Ingredients </p>
				<ul>
					<li> Bread - country white, sourdough, baguettes, or other white bread from the grocery store. It does not have to be fresh - buying from the discount rack might be a good idea here! </li>
					<li> Celery, onions, carrots, garlic </li>
					<li> Eggs </li>
					<li> Butter </li> 
					<li> Rosemary, sage, thyme </li> 
					<li> Salt, pepper </li>
					<li> Vegetable stock/turkey stock </li>
				</ul>
				
				<p> Instructions </p> 
				<ol>
					<li> The night before, tear the bread up. I usually like to use a knife to slice the bread into thick, 3/4inch pieces, before tearing into rough squares by hand. That way, the bread comes out especially craggly, with lots of uneven surfaces to catch liquid and to brown. Set the bread aside to get a bit more stale - I put it in baking pans and pop it in the oven, to keep safe. </li>
					<li> Melt a stick or two of butter, and soften all of your vegetables. You probably want to cook the carrots first, followed by onions, celery, and garlic, to get a reasonably soft vegetable mixture. Add in salt, pepper, and herbs (destemmed) towards the end to taste. </li>
					<li> In a large mixing bowl, put in the stale bread. Pour over your vegetable mixture, two beaten eggs, and the vegetable or turkey stock. Roughly mix, and lay out the bread into a large baking pan. Cover with aluminum foil. </li> 
					<li> Pop into a 350 degree oven for 20ish minutes, and then remove the aluminum foil. Take it out of the oven when the tops are sufficiently browned to your liking. </li> 
				</ol> 
				
				<p> Cranberry Sauce </p>
				
				<p> I think cranberry sauce was the first dish I was allowed to help prepare, at least after we moved away from the canned cranberry jelly. This is an incredibly simple process, and provides you with a lot more control over customizing the sauce, especially in controlling the amount of sugar that goes into it. </p>
				
				<p> Ingredients </p>
				<ul> 
					<li> 1 packet of cranberries, about 4oz </li>
					<li> 1/2 cup of water </li>
					<li> 1/2 cup of orange juice </li>
					<li> 1/2 cup of white sugar </li> 
					<li> 1/2 cup of brown sugar </li>
					<li> Cinnamon, orange peel, lemon peel, lemon juice </li> 
				</ul>
				
				<p> Instructions </p>
				<ol>
					<li> Dump the cranberries, water, juice, sugars, and additional flavorings into a large sauce pan. Start boiling the mixture over low heat, stirring occasionally. </li>
					<li> Note that as the cranberries cook, they will sometimes pop quite suddenly. Be aware of this, and stir somewhat frequently as they do so. Taste and adjust sugar as desired. </li>
					<li> After about 20 minutes or so, most of the cranberries will have popped, but the liquid might still seem a bit runny. Eventually, the pectin in the cranberries will make the mixture become more solid, so there is no need to constantly let it boil. </li> 
				</ol>
				
				<p> Mashed Potatoes </p>
				
				<p> This really is not strictly a thanksgiving dish, but it does seem like a classic at a large thanksgiving dinner, especially with the turkey gravy! </p>
				
				<p> Ingredients </p>
				<ul>
					<li> Russet potatoes </li>
					<li> Apple cider vinegar </li>
					<li> Butter </li>
					<li> Garlic </li>
					<li> Heavy cream, milk </li>
					<li> Salt, pepper </li>
					<li> Cheddar cheese, smoked paprika, chives </li>
				<ul>
				
				<p> Instructions </p>
				<ol>
					<li> Peel and chop potatoes into large chunks. The better you chunk your potatoes, the more uniformly they can cook. </li>
					<li> Throw the potatoes with water into a large pot and bring to a boil. Add a few pinches of salt. </li>
					<li> When the potatoes are tender (able to be easily pierced with a chopstick with little resistance), drain the potatoes in the sink. Reserve some of the liquid - using the starchy liquid is useful if your mixture is too dry.  While allowing the potatoes to cool slightly, give a few dashes of apple cider vinegar over the top, just to give this rich dish a hint of acid. </li>
					<li> While your potatoes cool, melt your butter and add in your garlic. Be careful to not burny your butter or garlic. </li>
					<li> Return your potatoes to the pot, and using a wooden spoon, roughly mash the potatoes. Add in your butter/garlic mixture, a few splashes of heavy cream, and maybe half a cup of milk. Keep adding in milk and the reserved water mixture until you reach your desired consistency. Keep over low heat during this process. </li>
					<li> Add in salt and pepper to taste. For me, I highly prefer dumping just a boatload of smoked paprika in, as well as adding a bit of preshredded cheddar cheese (you probably don't really need to break out the fancy stuff for this dish). </li>
				</ol>
				
