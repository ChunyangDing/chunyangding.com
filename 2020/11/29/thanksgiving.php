				<h2 id="stuffing"> Stuffing </h2>
				
				<p> Stuffing is definitely my favorite part of the thanksgiving meal - it has a lovely amount of variety, and you can spruce it up however you like! </p>
				
				<p> Ingredients </p>
				<ul>
					<li> Bread - country white, sourdough, baguettes, or other white bread from the grocery store. It does not have to be fresh - buying from the discount rack might be a good idea here! </li>
					<li> Celery, onions, carrots, garlic </li>
					<li> Eggs </li>
					<li> Butter </li> 
					<li> Rosemary, sage, thyme </li> 
					<li> Salt, pepper </li>
					<li> Vegetable stock/turkey stock </li>
				</ul>
				
				<p> Instructions </p> 
				<ol>
					<li> The night before, tear the bread up. I usually like to use a knife to slice the bread into thick, 3/4inch pieces, before tearing into rough squares by hand. That way, the bread comes out especially craggly, with lots of uneven surfaces to catch liquid and to brown. Set the bread aside to get a bit more stale - I put it in baking pans and pop it in the oven, to keep safe. </li>
					<li> Melt a stick or two of butter, and soften all of your vegetables. You probably want to cook the carrots first, followed by onions, celery, and garlic, to get a reasonably soft vegetable mixture. Add in salt, pepper, and herbs (destemmed) towards the end to taste. </li>
					<li> In a large mixing bowl, put in the stale bread. Pour over your vegetable mixture, two beaten eggs, and the vegetable or turkey stock. Roughly mix, and lay out the bread into a large baking pan. Cover with aluminum foil. </li> 
					<li> Pop into a 350 degree oven for 20ish minutes, and then remove the aluminum foil. Take it out of the oven when the tops are sufficiently browned to your liking. </li> 
				</ol> 

