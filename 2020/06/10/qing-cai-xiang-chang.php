				<h1>Shanghai Qingcai (上海青菜）</h1>
				
				<p> This is a dish that doesn't really need a recipe, but it is such a classic that I would be wont to not include it.  </p>
							
				<p> Ingredients </p>
				<ul>
					<li> Pork short ribs. You can get them pre-cut (ie, into 1-2 inch pieces), or buy them as long pieces and cleave them yourself. A meat cleaver is recommended, but you can make do with just a regular knife. </li>
					<li> Light and dark soy sauce </li>
					<li> Sugar </li>
					<li> Ginger </li>
					<li> Scallions </li> 
					<li> Shaoxing cooking wine </li>
					<li> Black Vinegar </li>
					<li> Sesame seeds, optional </li>
					<li> Oil </li>
				</ul> 
				
				<p> Instructions </p> 
				<ol>
					<li> Cut the short ribs to two-inch pieces, and marinate with about a tablespoon of light soy sauce and Shaoxing cooking wine for ~15-30 minutes, as you prepare other things. </li>
					<li> Chop up the whites of the scallions and the ginger. Fry in oil for a few minutes, until fragrant. </li>
					<li> Briefly fry up the short ribs in a large wok, mostly to brown the outsides. I think a medium heat would probably be good. This doesn't have a huge effect on the end result, but it is nicer to have it be crispy earlier on. </li>
					<li> Clean out the wok and add in a few tablespoons of oil. Very, very carefully, dissolve sugar in the oil. You want the heat to be fairly low during this process, because if overheated, the sugar can very easily burn. Also be very careful about not getting any water in to the mixture - the sugar/oil mixture has a tendancy to explode outwards, and the sugar will instantly cool and harden on any surface it comes in contact with. As soon as the sugar is partialy dissolved, add in the short ribs slowly, one at a time. Coat the short ribs with the sugar mixture, to create a crust on them. Don't overcrowd the pan here - I used chopsticks and closely monitored the process, picking them out and placing them into side bowls if they seemed to be too well done. </li>
					<li> After all short ribs are done, add everything into the pot - the ribs, the light and dark soy sauce, the precooked scallion/ginger mixture, another tablespoon of cooking wine, black vinegar, and two cups of water. Bring to a high boil, and simmer for at least 30 minutes. As you add sauces, taste to see if it is okay, and balance things out. </li>
					<li> Lower the heat and continue to simmer as the sauce thickens. It should become fairly syrupy, with the amount of sugar in the mixture. There will also be quite a bit of oil, both from the short-ribs that were cooked out, as well as the frying oil that was initially used. I have a tendancy to try to get rid of some of that oil - I found that the best way to do so was to pour out all of the sauce into a jar, and skim the top of that jar. </li> 
					<li> When ready to serve, retoss the short ribs with the sauce to make sure that everything is fully coated. Toast some sesame seeds and sprinkle on top. </li> 
				</ol> 
				
