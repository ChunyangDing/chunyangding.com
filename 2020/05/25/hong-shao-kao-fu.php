				<h1>Hongshao kaofu (红烧烤麸）</h1>
				
				<p> This was my favorite dish as a kid, and is still in my top five of all time. It is quintessentially Shanghainese, great hot or cold, and not difficult to put together (much to my surprise the first time I made it!) The only thing to keep in mind is, if you are using the dried versions of the ingredients, to presoak everything for at least an hour, and preferrably overnight. </p>
				
				<p> I don't think you really need proportions for any part of this dish. For the sauces, just constantly taste the mixture until it seems like something you want to eat. For the proportion of kaofu, wood's ear, mushrooms, and peanuts, again just add what you want to eat! </p> 
				
				<p> Ingredients </p>
				<ul>
					<li> Kaofu, or baked gluten. Only found in Chinese supermarkets, such as Great Wall or 99 Ranch. I prefer the dried variety, because it gives me more control of how much to use. However, the frozen type is just as good. </li>
					<li> Wood's Ear (黑木耳） - usually found in dried packs. </li>
					<li> Fragrant mushrooms (香菇） - also the dried packs that have quite a strong smell. </li>
					<li> Peanuts - again, from Chinese supermarkets, where they usually come with the pinkish shell. Keep that. </li>
					<li> Oil </li> 
					<li> Light and dark soy sauce </li>
					<li> Cooking wine - I prefer using the Shaoxing brand </li>
					<li> Sugar </li>
				</ul> 
				
				<p> Instructions </p> 
				<ol>
					<li> As previously mentioned, make sure that everything is fully soaked if dry. The wood's ear in particular tends to absorb a ton of water - feel free to drain and fill with more water as you see fit. I usually keep the soaking liquid from the fragrant mushrooms, because it makes for a good starter to the rest of the dish. If the kaofu is in large pieces, cut them to roughly 1 inch cubes, although you don't need to be exact. </li>
					<li> In a dry wok, add oil and kaofu over medium heat. The goal is to give the kaofu a bit of a crunchy exterior, so just a bit of oil is usually enough. </li>
					<li> While kaofu is frying, do the tedious task of cutting off the stems of the wood's ear. You can somewhat identify an area that is more "leafy", and a part that feels more "gritty" or "stemmy". Use scissors to cut them off if you would like - I don't think it is harmful to eat it, but it is not a very pleasant texture to bite into something that suddenly crunches unexpectedly. </li>
					<li> When the kaofu seems nicely seared on at least two sides, go ahead and add the rest of the ingredients in (wood's ear, mushrooms, peanuts). Keep the heat fairly high, and add in the light and dark soy sauces, cooking wine, sugar, and reserved mushroom water. There should be a fair amount of sauce/soup in the pot. Adjust the taste until you get a fairly strong sense of unami - not too salty, not to sweet. </li>
					<li> After bringing the liquid to a low boil, turn down and allow to simmer for about an hour. The goal here is to cook the mushrooms a bit more, and ensure that the kaofu really absorbs the flavor of the soup. Using a wooden spoon, stir the entire mixture around maybe every 10 minutes or so. Taste frequently! </li>
					<li> The dish is mostly done whenever you want to call it done. Serve cold for the traditional method, but it tastes fine hot as well. Maybe drizzle on a tad bit of sesame oil if you are feeling really fancy. </li>
				</ol> 
				
